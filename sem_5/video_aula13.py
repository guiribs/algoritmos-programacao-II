# Heap sort: utiliza uma estrutura heap (arvore binaria)

"""Um heap observa conceitos de ordem e de
forma.
- Ordem: o item de qualquer nó deve satisfazer
uma relação de ordem com os itens dos nós
filhos.
- Heap máximo: pai >= filhos
- Heap mínimo: pai <= filhos
- Forma: árvore binária tem que ser completa
até o penúltimo nível, sendo que no último
nível os nós têm que estar agrupados à
esquerda."""

